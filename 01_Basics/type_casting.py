# String to Integer
num1 = "100"
print("String:", num1)

num2 = int(num1)
print("Integer:", num2)

# Integer to Float
age = 22
print(float(age))

# Float to Integer
cgpa = 8.21
print(int(cgpa))

# Integer to String
number = 50
print(str(number))
