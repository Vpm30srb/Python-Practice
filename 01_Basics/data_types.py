# Integer
num = 100

# Float
price = 99.99

# String
course = "Python"

# Boolean
is_completed = False

# List
languages = ["Python", "Java", "JavaScript"]

# Tuple
coordinates = (10, 20)

# Dictionary
student = {
    "Name": "Vinayaka",
    "CGPA": 8.21
}

# Set
numbers = {1, 2, 3, 4}

print(type(num))
print(type(price))
print(type(course))
print(type(is_completed))
print(type(languages))
print(type(coordinates))
print(type(student))
print(type(numbers))
